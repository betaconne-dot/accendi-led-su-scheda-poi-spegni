input.onButtonPressed(Button.A, function () {
    led.plot(0, 0)
    led.setBrightness(255)
    basic.pause(2000)
    led.plot(1, 1)
    led.setBrightness(255)
    basic.pause(2000)
    led.plot(2, 2)
    led.setBrightness(255)
    basic.pause(2000)
    led.plot(3, 3)
    led.setBrightness(255)
    basic.pause(2000)
    led.plot(4, 4)
    led.setBrightness(255)
})
input.onButtonPressed(Button.B, function () {
    led.unplot(4, 4)
    basic.pause(2000)
    led.unplot(3, 3)
    basic.pause(2000)
    led.unplot(2, 2)
    basic.pause(2000)
    led.unplot(1, 1)
    basic.pause(2000)
    led.unplot(0, 0)
})
